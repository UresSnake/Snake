package matek;


public class Muveletek {

    public static double osszeadas(double a, double b)
    {
        return a+b;
    }
    public static double kivonas(double a, double b)
    {
        return a-b;
    }
    public static double szorzas(double a, double b)
    {
        return a*b;
    }
 
}
